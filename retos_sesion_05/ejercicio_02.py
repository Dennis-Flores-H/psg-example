# Definición de las temperaturas en grados Celsius
temp1 = 30
temp2 = -273.99
temp3 = 100

# Conversión a grados Fahrenheit utilizando la fórmula F = (9/5) * C + 32
fahrenheit1 = (9/5) * temp1 + 32
fahrenheit2 = (9/5) * temp2 + 32
fahrenheit3 = (9/5) * temp3 + 32

# Imprimir los resultados
print(temp1, "°C equivale a", fahrenheit1, "°F")
print(temp2, "°C equivale a", fahrenheit2, "°F")
print(temp3, "°C equivale a", fahrenheit3, "°F")