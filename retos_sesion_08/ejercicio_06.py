# Definir las coordenadas como tuplas
coordenada1 = (40, 80)
coordenada2 = (50, 50)

# Extraer las coordenadas individuales
x1, y1 = coordenada1
x2, y2 = coordenada2

# Calcular el punto medio
x = (x1 + x2) / 2
y = (y1 + y2) / 2

# Almacenar en una tupla
punto_medio = (x, y)

# Imprimir el resultado
print("Coordenadas del punto medio:")
print(punto_medio, type(punto_medio))
