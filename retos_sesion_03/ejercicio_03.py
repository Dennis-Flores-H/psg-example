print("Tutorial para barrer una habitación")

print("Paso 1: Preparativos")
print("1. Asegúrate de que tienes una escoba y un recogedor.")
print("2. Si tienes muebles pequeños, muévelos para facilitar el barrido.")
print("3. Si hay alfombras, sacúdelas afuera antes de empezar.")

print("\nPaso 2: Agarra la escoba correctamente")
print("1. Coloca una mano en el medio del palo de la escoba.")
print("2. La otra mano debe ir más cerca del extremo del palo, para tener un buen control.")

print("\nPaso 3: Comienza a barrer")
print("1. Empieza a barrer desde una esquina de la habitación.")
print("2. Usa movimientos largos y fluidos, arrastrando la escoba hacia ti.")
print("3. Dirige la suciedad hacia el centro de la habitación, formando una pila.")

print("\nPaso 4: Barrer debajo de los muebles")
print("1. Inclina la escoba para alcanzar debajo de los muebles.")
print("2. Asegúrate de barrer bien debajo de las sillas, mesas y sofás.")

print("\nPaso 5: Formar una pila de suciedad")
print("1. Continúa barriendo toda la habitación, siempre dirigiendo la suciedad hacia la pila central.")
print("2. Asegúrate de que todas las esquinas y bordes están limpios.")

print("\nPaso 6: Usar el recogedor")
print("1. Coloca el recogedor en el suelo, justo en el borde de la pila de suciedad.")
print("2. Usa la escoba para barrer la suciedad hacia el recogedor, haciéndolo con movimientos firmes y controlados.")
print("3. Asegúrate de que toda la suciedad entra en el recogedor. Repite si es necesario.")

print("\nPaso 7: Eliminar la basura")
print("1. Vacía el contenido del recogedor en una bolsa de basura.")
print("2. Si hay residuos pequeños en el recogedor, puedes usar una toalla de papel para limpiarlos.")
print("3. Asegúrate de cerrar bien la bolsa de basura y llévala al contenedor de basura.")

print("\nPaso 8: Guardar los utensilios")
print("1. Guarda la escoba y el recogedor en su lugar correspondiente.")
print("2. Lávate las manos después de terminar el trabajo.")